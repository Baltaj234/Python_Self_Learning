# Exploring how casting works in python

y = int(1) # Number will be 1

x = int(2.8) # Number will be 2

z = int("3") # Number will be 3

# Doing the same thing with floats

d = float(1) # Will be 1.0

e = float(2.8) # Will stay 2.8

w = float("3") # Will be 3.0

q = float("4.2") # Will still be 4.2

# Doing the same thing with strings

string = str(2) # Will be '2'

string2 = str(3.0) # will be '3.0'
