# Using Python built in methods to modify strings

# Making strings uppercase
a = "Hello World"
print(a.upper())

# Making strings lowercase

a = "Hello World"
print(a.lower())

# Removing whitepspace from a string
a = "  Hello   World  "
print(a.strip())

# Replacing a string with another string (in the example a letter)
a = "Hello World"
# Replacing H with J
print(a.replace("H", "J"))





